"""
§0.2 统一错误码（与《系统接口规范》一致）。
"""

# 成功
EC_OK = 200

# 40001-40099 参数错误
EC_PARAM = 40001

# 40100-40199 认证/鉴权
EC_AUTH_REQUIRED = 40100
EC_AUTH_EXPIRED = 40101
EC_AUTH_INVALID = 40102
EC_AUTH_USER_MISMATCH = 40103
EC_AUTH_FORBIDDEN_TASK = 40104

# 40400-40499 资源不存在
EC_NOT_FOUND = 40400

# 40900-40999 冲突
EC_CONFLICT = 40900

# 50000-50099 服务内部错误
EC_INTERNAL = 50000

# 50200-50299 依赖不可用
EC_DEPENDENCY = 50200  # 通用（Redis 等）
EC_LLM_DEPENDENCY = 50210  # 模型/LLM 调用失败（同步路径）
